package org.eclipse.scanning.api.points.models;


/**
 * Using either null or EmptyModel will give one empty position from the generator.
 * This can be used to run devices without an outer scan, from the outer scanning.
 * 
 * These devices may also be used directly.
 * 
 * @author Matthew Gerring
 *
 */
public class EmptyModel extends AbstractPointsModel {

	@Override
	public String getName() {
		return "Empty";
	}

}
